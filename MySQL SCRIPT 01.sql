/* IDS E EMAIL DAS MULHERES QUE MOREM NO CENTRO DO RJ E QUE
NÃO TENHAM EMAIL */

SELECT CL.IDCLIENTE,CL.NOME,CL.EMAIL,CL.SEXO,EN.BAIRRO,TE.NUMERO
FROM CLIENTE CL
INNER JOIN ENDEREÇO EN
ON CL.IDCLIENTE = EN.ID_CLIENTE
INNER JOIN TELEFONE TE
ON CL.IDCLIENTE = TE.ID_CLIENTE
WHERE CL.SEXO = 'F' AND EN.BAIRRO = 'CENTRO'
AND EN.CIDADE = 'Rio de Janeiro'
AND CL.EMAIL IS NULL;

