/* NOME, SEXO APENAS DE MULHERES, BAIRRO, CIDADE (JUNÇÃO DE TABELAS) */

SELECT NOME, SEXO, BAIRRO, CIDADE
FROM CLIENTE
INNER JOIN ENDEREÇO
ON IDCLIENTE = ID_CLIENTE
WHERE SEXO = 'F';

/* NOME, SEXO, EMAIL, TIPO, NÚMERO */

SELECT NOME, SEXO, EMAIL, TIPO, NUMERO
FROM CLIENTE
INNER JOIN TELEFONE
ON IDCLIENTE = ID_CLIENTE
WHERE TIPO = 'CEL';

/* EXERCÍCIO -> RETORNAR APENAS NOME (FORMATO NOME_PARTICIPANTE) EM ORDEM ALFABÉTICA
COM SEXO E TELEFONE*/

SELECT NOME AS NOME_PARTICIPANTE, SEXO, NUMERO AS TELEFONE
FROM CLIENTE
INNER JOIN TELEFONE
ON IDCLIENTE = ID_CLIENTE
ORDER BY NOME_PARTICIPANTE;

/* EXERCICIO -> RETORNAR NOME, QUANTIDADE DE TELEFONE, E ORDENAR
POR QUANTIDADE MAIOR DE TELEFONE */

SELECT NOME, COUNT(NUMERO) AS TELEFONE
FROM CLIENTE
INNER JOIN TELEFONE
ON IDCLIENTE = ID_CLIENTE
GROUP BY NOME
ORDER BY NUMERO DESC;

/* NOME, SEXO, BAIRRO, CIDADE, TIPO, NUMERO (JUNÇÃO DE 3 TABELAS) */

SELECT CL.NOME,CL.SEXO,EN.BAIRRO,EN.CIDADE,TE.TIPO,TE.NUMERO
FROM CLIENTE CL
INNER JOIN ENDEREÇO EN
ON CL.IDCLIENTE = EN.ID_CLIENTE
INNER JOIN TELEFONE TE
ON CL.IDCLIENTE = TE.ID_CLIENTE;


